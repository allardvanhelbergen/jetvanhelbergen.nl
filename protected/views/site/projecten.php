          <p>
            Wat is dat thuisland waar Belafonte over zingt? En hoe komt het dat
            zijn verlangen zo herkenbaar is? Is de zoektocht naar het thuisland
            universeel en van alle tijden? Of is deze verbonden aan de
            globalisatie waar wij vandaag de dag deel van uitmaken? Hoe zoekt
            de ontheemde mens anno 2012 naar de plek waar hij thuis hoort? En
            is deze plek überhaupt bereikbaar, of is het thuisland een utopie?  
          </p>
          <p>
            Jet van Helbergen maakt in samenwerking met schrijfster Marjolijn
            van Heemstra een voorstelling over het grotere verhaal achter dit
            liedje. Een voorstelling over heimwee en verlangen, in alle soorten
            en maten. Met bestaande en nieuw gecomponeerde muziek. Locatie is
            de stille Palmenkas in de Amsterdamse Hortus, vol bomen die
            afkomstig zijn uit andere thuislanden en op hun eigen manier
            verstekelingen.
