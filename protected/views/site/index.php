      <h2>Biografie</h2>
      <p>
        Jet van Helbergen vatte al vroeg een liefde op voor muziek en
        zong vanaf haar achtste in kinderkoren. Tijdens haar studie
        kunstgeschiedenis aan de Universiteit van Leiden studeerde zij
        ook klassiek zang bij Lenie van den Heuvel aan het
        Conservatorium van Den Haag. Na het halen van haar master
        kunstgeschiedenis besloot ze zich volledig op muziek te richten
        en verder te studeren bij Frans Huijts aan het Conservatorium
        van Rotterdam, waar zij in 2009 haar bachelor studie klassiek
        zang voltooide. Zij volgde masterclasses en workshops bij o.a.
        Meinard Kraak, Carolyn Watkinson, Roberta Alexander, John Dawson
        en Rudolf Piernay.
      </p>
      <p>
        De dramaturgische aspecten van het zangvak spreken Van Helbergen
        bijzonder aan. Zo nam zij deel aan de muziektheater produktie
        <em>Sleeping Beauty</em> 
        onder leiding van Cilia Hogerzeil, uitgevoerd tijdens het
        Dordtse festival
        <em>Voor het Voetlicht</em>.
        Tevens besloot zij zich op priv&eacute;-basis verder te scholen
        bij acteur en regisseur Luc Boyer om haar dramatische expressie
        verder te ontwikkelen.
      </p>
      <p>
        Solistisch zong zij onder andere de rol van koorsoliste in de
        opera
        <em>Lege Wie, Bo&scedil; Be&scedil;ik</em>
        van Seung-Ah Oh bij het Muziektheater Hollands Diep. Ook was zij
        te horen als Dido in de opera
        <em>Dido and Aeneas</em>
        van H. Purcell in Concertgebouw de Doelen o.l.v. Philip Pickett
        en als Geraldine in de mini-opera
        <em>A hand of Bridge</em>
        van S. Barber o.l.v. Hans Leenders.
      </p>
      <p>
        Discipline overschrijdende projecten hebben haar interesse. Zo
        verleende zij haar muzikale medewerking  aan community arts
        projecten van Yo! Opera en Stichting Accu. Ook werkte zij samen
        met Brigitte Kaandorp in het theaterprogramma
        <em>de Parelvissers</em>,
        een stoomcursus opera. Met Koor de Stemming zet Van Helbergen
        zich in om muziek te brengen op locaties waar dat niet altijd
        vanzelfsprekend is, zoals weeshuizen, daklozenopvang en
        bejaardetehuizen. Regelmatig voert zij liederenprogramma&apos;s
        uit, waarbij de muziek in combinatie met culturele en
        kunsthistorische achtergrond wordt gepresenteerd en op die
        manier in een bredere context wordt geplaatst.
      </p>
